'''7. Perform packing and unpacking on a tuple containing the names of three fruits: 'apple', 'banana', and 'orange'. 
Use unpacking to assign each fruit to three variables: fruit1, fruit2, and fruit3.

       Expected Input: No input required.
       Expected Output:

- fruit1 = 'apple'
- fruit2 = 'banana'
- fruit3 = 'orange'
'''
# Packing: Create a tuple with the names of three fruits
fruits_tuple = ('apple', 'banana', 'orange')

# Unpacking: Assign each fruit to a separate variable
fruit1, fruit2, fruit3 = fruits_tuple

# Print the results
print(f"fruit1 = '{fruit1}'")
print(f"fruit2 = '{fruit2}'")
print(f"fruit3 = '{fruit3}'")

